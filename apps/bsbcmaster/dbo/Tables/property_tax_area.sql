CREATE TABLE [dbo].[property_tax_area] (
    [year]                NUMERIC (4) NOT NULL,
    [sup_num]             INT         NOT NULL,
    [prop_id]             INT         NOT NULL,
    [tax_area_id]         INT         NOT NULL,
    [tax_area_id_pending] INT         NULL,
    [effective_date]      DATETIME    NULL,
    [is_annex_value]      BIT         CONSTRAINT [CDF_property_tax_area_is_annex_value] DEFAULT ((0)) NOT NULL,
    CONSTRAINT [CPK_property_tax_area] PRIMARY KEY CLUSTERED ([year] ASC, [sup_num] ASC, [prop_id] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [CCK_property_tax_area_pending] CHECK (isnull([tax_area_id_pending],(0))<>[tax_area_id]),
    CONSTRAINT [CFK_property_tax_area_tax_area_id] FOREIGN KEY ([tax_area_id]) REFERENCES [dbo].[tax_area] ([tax_area_id])
);


GO

CREATE NONCLUSTERED INDEX [IX_PropID_property_tax_area]
    ON [dbo].[property_tax_area]([prop_id] ASC);


