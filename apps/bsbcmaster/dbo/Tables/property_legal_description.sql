CREATE TABLE [dbo].[property_legal_description] (
    [prop_val_yr]      NUMERIC (4)   NOT NULL,
    [sup_num]          INT           NOT NULL,
    [prop_id]          INT           NOT NULL,
    [metes_and_bounds] VARCHAR (MAX) NULL,
    CONSTRAINT [CPK_property_legal_description] PRIMARY KEY CLUSTERED ([prop_val_yr] ASC, [sup_num] ASC, [prop_id] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [CFK_property_legal_description_prop_val_yr_sup_num_prop_id] FOREIGN KEY ([prop_val_yr], [sup_num], [prop_id]) REFERENCES [dbo].[property_val] ([prop_val_yr], [sup_num], [prop_id])
);


