CREATE TABLE [dbo].[master_lease_prop_assoc] (
    [group_id] INT         NOT NULL,
    [year]     NUMERIC (4) NOT NULL,
    [prop_id]  INT         NOT NULL,
    CONSTRAINT [CPK_master_lease_prop_assoc] PRIMARY KEY CLUSTERED ([group_id] ASC, [year] ASC, [prop_id] ASC),
