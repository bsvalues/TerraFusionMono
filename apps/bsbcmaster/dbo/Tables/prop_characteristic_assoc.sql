CREATE TABLE [dbo].[prop_characteristic_assoc] (
    [prop_id]           INT          NOT NULL,
    [prop_val_yr]       NUMERIC (4)  NOT NULL,
    [sup_num]           INT          NOT NULL,
    [sale_id]           INT          CONSTRAINT [CDF_prop_characteristic_assoc_sale_id] DEFAULT ((0)) NOT NULL,
    [characteristic_cd] VARCHAR (10) NOT NULL,
    [attribute_cd]      VARCHAR (20) NOT NULL,
    CONSTRAINT [CPK_prop_characteristic_assoc] PRIMARY KEY CLUSTERED ([prop_val_yr] ASC, [sup_num] ASC, [sale_id] ASC, [prop_id] ASC, [characteristic_cd] ASC, [attribute_cd] ASC) WITH (FILLFACTOR = 90),
    CONSTRAINT [CFK_prop_characteristic_assoc_characteristic_cd_attribute_cd] FOREIGN KEY ([characteristic_cd], [attribute_cd]) REFERENCES [dbo].[attribute_value_code] ([characteristic_cd], [attribute_cd])
);


GO

